package modelo;

public class Empresa {
    String id_empresa;
    String nombre;

    public Empresa() {
    id_empresa="";
    nombre="";
    }

    public String getId_empresa() {
        return id_empresa;
    }

    public void setId_empresa(String id_empresa) {
        this.id_empresa = id_empresa;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

   
   
    
   
    
    
}
